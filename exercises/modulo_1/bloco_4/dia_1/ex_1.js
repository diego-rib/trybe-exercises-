let a = 10;
let b = 2;

// Adição
let soma = a + b;
console.log(soma);

// Subtração
let diferenca = a - b;
console.log(diferenca);

// Multiplicação
let produto = a * b;
console.log(produto);

// Divisão
let quociente = a / b;
console.log(quociente);

// Módulo
let modulo = a % b;
console.log(modulo);
